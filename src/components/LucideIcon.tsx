interface LucideIconProps extends React.SVGProps<SVGSVGElement> {
  name: string;
  size?: number | string;
  className?: string;
}

export default function LucideIcon({ name, size = 20, className, ...props }: LucideIconProps) {
  const IconComponent = iconMap[name];
  if (!IconComponent) {
    // Return a default code icon if the name isn't mapped
    return <Code size={size} className={className} {...props} />;
  }
  return <IconComponent size={size} className={className} {...props} />;
}
